public class Calculator {

    public static int adds(int num1, int num2){
        return num1 + num2;
    }

    public static int adds(int num1, int num2, int num3){
        return num1 + num2 + num3;
    }

    public static float adds(float num1, float num2, float num3){
        return num1 + num2 + num3;
    }

    public static float adds(float num1, float num2){
        return num1 + num2 ;
    }

    public static float adds(int num1, float num2){
        return num1 + num2;
    }
}
